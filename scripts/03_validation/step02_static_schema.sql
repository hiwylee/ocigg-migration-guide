-- [검증항목 SS-019~023] 시퀀스 및 Synonym 검증
PROMPT
PROMPT --- 시퀀스 전체 속성 비교 ---
SELECT SEQUENCE_OWNER, SEQUENCE_NAME,
       MIN_VALUE, MAX_VALUE, INCREMENT_BY,
       CYCLE_FLAG, ORDER_FLAG, CACHE_SIZE, LAST_NUMBER
FROM DBA_SEQUENCES
WHERE SEQUENCE_OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS')
ORDER BY SEQUENCE_OWNER, SEQUENCE_NAME;
-- 타겟 LAST_NUMBER ≥ 소스 LAST_NUMBER + CACHE_SIZE 확인
-- INCREMENT_BY / CYCLE / ORDER / CACHE 소스와 완전 일치 확인

PROMPT
PROMPT --- Synonym 전수 비교 (Public 포함) ---
SELECT OWNER, SYNONYM_NAME, TABLE_OWNER, TABLE_NAME, DB_LINK
FROM DBA_SYNONYMS
WHERE (OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS') OR OWNER = 'PUBLIC')
ORDER BY OWNER, SYNONYM_NAME;
-- 소스 MINUS 타겟 → 기대값: 0건

PROMPT
PROMPT --- 참조 객체 없는 Synonym (Dangling Synonym) ---
PROMPT [타겟] 실행
SELECT S.OWNER, S.SYNONYM_NAME, S.TABLE_OWNER, S.TABLE_NAME
FROM DBA_SYNONYMS S
WHERE NOT EXISTS (
    SELECT 1 FROM DBA_OBJECTS O
    WHERE O.OWNER = S.TABLE_OWNER AND O.OBJECT_NAME = S.TABLE_NAME
)
AND S.DB_LINK IS NULL
AND S.OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS');
-- 기대값: 0건

