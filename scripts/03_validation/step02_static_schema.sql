-- [검증항목 SS-006~009] 인덱스 완전성 검증
PROMPT
PROMPT --- ① 인덱스 전체 목록 비교 (Invisible 포함) ---
SELECT OWNER, INDEX_NAME, TABLE_NAME, INDEX_TYPE, UNIQUENESS, STATUS, VISIBILITY, PARTITIONED
FROM DBA_INDEXES
WHERE OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS')
ORDER BY OWNER, TABLE_NAME, INDEX_NAME;
-- 소스 MINUS 타겟 → 기대값: 0건

PROMPT
PROMPT --- ② 인덱스 컬럼 구성 비교 (컬럼 순서 포함) ---
SELECT INDEX_OWNER, INDEX_NAME, TABLE_NAME, COLUMN_POSITION, COLUMN_NAME, DESCEND
FROM DBA_IND_COLUMNS
WHERE INDEX_OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS')
ORDER BY INDEX_OWNER, INDEX_NAME, COLUMN_POSITION;

PROMPT
PROMPT --- ③ Function-Based Index (FBI) 표현식 비교 ---
SELECT INDEX_OWNER, INDEX_NAME, TABLE_NAME, COLUMN_POSITION, COLUMN_EXPRESSION, DESCEND
FROM DBA_IND_EXPRESSIONS
WHERE INDEX_OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS')
ORDER BY INDEX_OWNER, INDEX_NAME, COLUMN_POSITION;

PROMPT
PROMPT --- ④ 전체 인덱스 VALID 상태 (UNUSABLE 0건) ---
PROMPT [타겟] 실행
