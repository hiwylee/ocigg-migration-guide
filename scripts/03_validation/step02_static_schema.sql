-- [검증항목 SS-001~005] 테이블/컬럼 구조 비교
PROMPT
PROMPT --- 테이블/컬럼 전수 목록 (소스/타겟 각각 실행 후 MINUS 비교) ---
SELECT OWNER, TABLE_NAME, COLUMN_NAME, COLUMN_ID,
       DATA_TYPE, DATA_LENGTH, DATA_PRECISION, DATA_SCALE, NULLABLE, DATA_DEFAULT
FROM DBA_TAB_COLUMNS
WHERE OWNER NOT IN (
    'SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS','AUDSYS','CTXSYS',
    'DBSFWUSER','DVSYS','GGSYS','GSMADMIN_INTERNAL','LBACSYS',
    'MDSYS','OJVMSYS','OLAPSYS','ORDDATA','ORDSYS','WMSYS','XDB','XS$NULL'
)
ORDER BY OWNER, TABLE_NAME, COLUMN_ID;
-- 소스 결과 MINUS 타겟 결과 → 기대값: 0건

PROMPT
PROMPT --- 테이블 압축 속성 비교 ---
SELECT OWNER, TABLE_NAME, COMPRESSION, COMPRESS_FOR
FROM DBA_TABLES
WHERE OWNER NOT IN ('SYS','SYSTEM','OUTLN','DBSNMP','APPQOSSYS')
AND COMPRESSION = 'ENABLED'
ORDER BY OWNER, TABLE_NAME;
-- 소스에 압축 테이블 존재 시 타겟도 동일 방식 적용 여부 확인

